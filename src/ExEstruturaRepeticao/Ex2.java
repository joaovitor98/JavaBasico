package ExEstruturaRepeticao;

import java.util.Scanner;

public class Ex2 {
    public static void main(String[] args) {
        Scanner leitura = new Scanner(System.in);
        int num = leitura.nextInt();
        int soma = 0;
        for (int i = soma; i <= num; i++) {
            soma += i;
        }
        System.out.println(soma);
    }
}
